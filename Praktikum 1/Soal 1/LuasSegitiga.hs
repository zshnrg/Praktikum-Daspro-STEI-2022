module LuasSegitiga where

-- LUAS SEGITIGA                        luasSegitiga(a,t)

-- DEFINISI DAN SPESIFIKASI
luasSegitiga :: Float -> Float -> Float
{- Menghitung luas segitiga berdasarkan input panjang alas dan tinggi -}

-- REALISASI
luasSegitiga a t = 0.5 * a * t

-- APLIKASI
-- luasSegitiga 10 10
